package io.github.codeblooded.model;

/**
 * Αντιπροσωπεύει έναν χρήστη του συστήματος κατά τη διάρκεια της συνεδρίας.
 *
 * <p>Η κλάση αυτή είναι υπεύθυνη για τον ορισμό των δικαιωμάτων πρόσβασης. Χρησιμοποιείται από την
 * {@link io.github.codeblooded.service.UserService} για να καθορίσει αν ο τρέχων χρήστης μπορεί να
 * επεξεργαστεί δεδομένα ή όχι.
 *
 * @author CodeBlooded Team
 * @version 1.0
 */
public class User {

  /** Οι διαθέσιμοι ρόλοι χρηστών στην εφαρμογή. */
  public enum Role {
    /** Διαχειριστής με πλήρη δικαιώματα (Ανάγνωση και Επεξεργασία). */
    ADMIN,
    /** Επισκέπτης με περιορισμένα δικαιώματα (Μόνο Ανάγνωση). */
    GUEST
  }

  /** Ο ρόλος του χρήστη (δεν αλλάζει μετά τη δημιουργία του αντικειμένου). */
  private final Role role;

  /**
   * Δημιουργεί έναν νέο χρήστη με τον καθορισμένο ρόλο.
   *
   * @param role Ο ρόλος που ανατίθεται στον χρήστη (ADMIN ή GUEST).
   */
  public User(Role role) {
    this.role = role;
  }

  /**
   * Επιστρέφει τον ρόλο του χρήστη.
   *
   * @return Το αντικείμενο {@link Role}.
   */
  public Role getRole() {
    return role;
  }

  /**
   * Βοηθητική μέθοδος ελέγχου δικαιωμάτων διαχειριστή.
   *
   * <p>Χρησιμοποιείται συχνά στους Controllers για να ενεργοποιήσουν ή να κρύψουν κουμπιά
   * επεξεργασίας.
   *
   * @return {@code true} αν ο χρήστης είναι ADMIN, αλλιώς {@code false}.
   */
  public boolean isAdmin() {
    return this.role == Role.ADMIN;
  }
}
