-- Ενεργοποίηση Foreign Key Constraint
PRAGMA foreign_keys = ON;

DROP TABLE IF EXISTS Budget;
DROP TABLE IF EXISTS Programs;
DROP TABLE IF EXISTS Ministries;
DROP TABLE IF EXISTS Regions;
DROP TABLE IF EXISTS Users;

-- Πίνακας Users
CREATE TABLE Users (
    user_id     INTEGER     PRIMARY KEY,
    username    TEXT        NOT NULL UNIQUE,
    role        TEXT        NOT NULL CHECK(role IN ('admin', 'readonly')) 
);

-- Πίνακας Ministries (Φορείς/Υπουργεία)
CREATE TABLE Ministries (
    ministry_id     INTEGER     PRIMARY KEY,
    ministry_name   TEXT        NOT NULL UNIQUE,
    total_budget_2025 REAL      -- Συνολικό budget του Υπουργείου (για αθροιστική αναφορά)
);

-- Πίνακας Regions (Αποκεντρωμένες Διοικήσεις)
CREATE TABLE Regions (
    region_id       INTEGER     PRIMARY KEY,
    region_name     TEXT        NOT NULL UNIQUE
);

-- Πίνακας Programs (Μεταδεδομένα & Tags)
CREATE TABLE Programs (
    program_id                  INTEGER     PRIMARY KEY,
    program_name                TEXT        NOT NULL UNIQUE,
    
    -- Σύνδεση με Ministries (για Κεντρικούς Φορείς) ή Regions (για Α.Δ.)
    ministry_id                 INTEGER, 
    region_id                   INTEGER,
    
    -- 6 Επιμέρους Στόχοι Περιβαλλοντικής Αποτίμησης
    tag_klhmatikh_meivsh        TEXT,          
    tag_klhmatikh_prosarmofh    TEXT,      
    tag_ydatina                 TEXT,                   
    tag_kyklikh_oikonomia       TEXT,         
    tag_rypansh_elegxos         TEXT,           
    tag_biopoikilothta_prostasla TEXT,  
    
    -- Συνολική Αποτίμηση (Green Tag): Περιλαμβάνει 'n/a'
    green_tag                   TEXT        NOT NULL CHECK(green_tag IN ('Θ', 'Α', 'Ο', 'ΜΠ', 'n/a')),
    
    FOREIGN KEY (ministry_id) REFERENCES Ministries(ministry_id),
    FOREIGN KEY (region_id) REFERENCES Regions(region_id)
);

-- Πίνακας Budget (Οικονομικά Στοιχεία)
CREATE TABLE Budget (
    id          INTEGER     PRIMARY KEY,
    -- Σύνδεση 1:1 με τον πίνακα Programs
    program_id  INTEGER     NOT NULL UNIQUE, 
    poso        REAL        NOT NULL CHECK(poso > 0), 
    pososto     REAL,       -- Υπολογίζεται από την εφαρμογή (Java)
    
    FOREIGN KEY (program_id) REFERENCES Programs(program_id)
);



